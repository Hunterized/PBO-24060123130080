    private void jButtonLoadListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoadListActionPerformed
        for (String item : items) {
            dlm.addElement(item);
        }
    }//GEN-LAST:event_jButtonLoadListActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        int index = jListItem.getSelectedIndex();
        String selected = jTextFieldItem.getText();
        dlm.setElementAt(selected, index);

        jTextFieldItem.setText("");
    }//GEN-LAST:event_jButtonUpdateActionPerformed

    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        addItem(jTextFieldItem.getText());
        jTextFieldItem.setText("");
    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        int index = jListItem.getSelectedIndex();
        if (index != -1) {
            String removedItem = dlm.getElementAt(index);
            dlm.removeElementAt(index);
            items.remove(removedItem); 
            updateJumDataTersimpan();
            jTextFieldItem.setText("");
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonLoadMapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoadMapActionPerformed
        for (Map.Entry<String, String> entry : itemMap.entrySet()){
            dlm.addElement(entry.getKey() + " : " + entry.getValue());
        }
    }//GEN-LAST:event_jButtonLoadMapActionPerformed

    private void jButtonSaveMapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveMapActionPerformed
        itemMap.clear();
        for (int i = 0; i < dlm.getSize(); i++){
            itemMap.put("Key" + i, dlm.getElementAt(i));
        }
        updateJumDataTersimpan();       // TODO add your handling code here:
    }//GEN-LAST:event_jButtonSaveMapActionPerformed

    private void jButtonSaveListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveListActionPerformed
        items.clear();
        for (int i = 0; i < dlm.getSize(); i++) {
            items.add(dlm.getElementAt(i));
        }
        updateJumDataTersimpan();
    }//GEN-LAST:event_jButtonSaveListActionPerformed

    private void jButtonLoadSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoadSetActionPerformed
        for (String item: itemSet){
            dlm.addElement(item);
        }
    }//GEN-LAST:event_jButtonLoadSetActionPerformed

    private void jButtonSaveSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveSetActionPerformed
        itemSet.clear();
        for (int i = 0; i < dlm.getSize(); i++) {
            itemSet.add(dlm.getElementAt(i));
        }
        updateJumDataTersimpan();
    }//GEN-LAST:event_jButtonSaveSetActionPerformed

