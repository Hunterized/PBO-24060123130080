    public void addItem(String namaItem) {
        if (!namaItem.isEmpty()) {
            dlm.addElement(namaItem);
            items.add(namaItem);
            updateJumDataTersimpan();
        }
    }
    public void updateJumDataTersimpan() {
        jLabelListData.setText("Data tersimpan = " + items.size());
        jLabelSetData.setText("Data tersimpan = " + itemSet.size());
        jLabelMapData.setText("Data tersimpan = " + itemMap.size());
    }
    
    

